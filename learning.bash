First let's push the backend code to GitHub.
1. Navigate to your project folder:
   cd path/to/backend

2. Stage and commit your changes:
   git add .
   git commit -m "Integrate DB"

3. Push your code to GitHub:
   git push -u origin main
   
   # Why? This uploads your local commits to the remote repo on GitHub, making them available for you or collaborators.

4. Verify on GitHub:
   # Refresh your repository page to see all the committed files.


In the same way, if you have any changes in the frontend code, you can push it to GitHub.